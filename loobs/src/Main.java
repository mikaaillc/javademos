public class Main {

    public static void main(String[] args) {
        //for
        for (int i = 0; i <=10; i+=2) {
            System.out.println(i);

        }
        System.out.println("Döbgü bitti...");

        //while
        int i =1;
        while (i<10){
            System.out.println(i);
            i+=2;
        }
        System.out.println("While Döngü bitti");
    }
}
